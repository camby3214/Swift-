import Foundation
import UIKit

extension UIFont {
    static func PGtext12() -> UIFont {
        return UIFont.systemFont(ofSize: 12)
    }
    static func PGtext14() -> UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    static func PGtext16() -> UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    static func PGtextBold12() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 12)
    }
    static func PGtextBold14() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 14)
    }
    static func PGtextBold16() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    static func PGtextHeader2() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 16)
    }
    static func PGtextHeader1() -> UIFont {
        return UIFont.boldSystemFont(ofSize: 20)
    }
}
